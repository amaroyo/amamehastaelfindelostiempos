package es.oyssen.mrm.struts.forms.asignaturas;

import es.oyssen.mrm.struts.forms.dhtmlx.DhtmlxGridForm;

public class GridUsuariosTrabajosCampoAsignaturaForm extends DhtmlxGridForm {

	private String idPortafolio;
	private String idTrabajoCampo;
	private String idUsuario;
	private String idAsignatura;
	private String nombreTrabajoCampo;
	private String apellidos;
	private String dni;
	private String fecha;
	private String enlace;
	
	
	
	public String getIdPortafolio() {
		return idPortafolio;
	}
	public void setIdPortafolio(String idPortafolio) {
		this.idPortafolio = idPortafolio;
	}
	public String getIdTrabajoCampo() {
		return idTrabajoCampo;
	}
	public void setIdTrabajoCampo(String idTrabajoCampo) {
		this.idTrabajoCampo = idTrabajoCampo;
	}
	public String getIdUsuario() {
		return idUsuario;
	}
	public void setIdUsuario(String idUsuario) {
		this.idUsuario = idUsuario;
	}
	public String getIdAsignatura() {
		return idAsignatura;
	}
	public void setIdAsignatura(String idAsignatura) {
		this.idAsignatura = idAsignatura;
	}
	public String getNombreTrabajoCampo() {
		return nombreTrabajoCampo;
	}
	public void setNombreTrabajoCampo(String nombreTrabajoCampo) {
		this.nombreTrabajoCampo = nombreTrabajoCampo;
	}
	public String getApellidos() {
		return apellidos;
	}
	public void setApellidos(String apellidos) {
		this.apellidos = apellidos;
	}
	public String getDni() {
		return dni;
	}
	public void setDni(String dni) {
		this.dni = dni;
	}
	public String getFecha() {
		return fecha;
	}
	public void setFecha(String fecha) {
		this.fecha = fecha;
	}
	public String getEnlace() {
		return enlace;
	}
	public void setEnlace(String enlace) {
		this.enlace = enlace;
	}
	
	
	
	
	
	
}
