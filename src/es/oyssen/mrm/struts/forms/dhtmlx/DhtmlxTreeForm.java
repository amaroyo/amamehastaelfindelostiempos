public class DhtmlxTreeForm extends DhtmlxForm {
	private String tr_id;
	private String tr_order;
	private String tr_pid;
	private String tr_text;
	
	public String getTr_id() {
		return tr_id;
	}
	public void setTr_id(String tr_id) {
		this.tr_id = tr_id;
	}
	public String getTr_order() {
		return tr_order;
	}
	public void setTr_order(String tr_order) {
		this.tr_order = tr_order;
	}
	public String getTr_pid() {
		return tr_pid;
	}
	public void setTr_pid(String tr_pid) {
		this.tr_pid = tr_pid;
	}
	public String getTr_text() {
		return tr_text;
	}
	public void setTr_text(String tr_text) {
		this.tr_text = tr_text;
	}
}
